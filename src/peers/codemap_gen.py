AGENTS_FILE = "AGENTS.md"

_AGENTS_HEADER = (
    "# AGENTS.md — verified codebase guide\n\n"
    "_Generated from CODEMAP.yaml (the drift-gated source of truth) — every\n"
    "entry below is anchored to a real symbol. Regenerate with `peers\n"
    "agents-doc`; the `agents-in-sync` gate fails if this file drifts from the\n"
    "CODEMAP. Do not hand-edit._\n"
)


def render_agents_md(codemap: CodeMap) -> str:
    """Render AGENTS.md — a module-organized, agent-facing guide — as a pure
    deterministic function of the verified CODEMAP. Each module is a section
    (its summary as intro), with its public classes (method signatures +
    summaries nested) and functions (signature + summary). No LLM: every line is
    anchored to a real CODEMAP entry, so a byte-equality gate (`check_agents_sync`)
    keeps it provably in sync with the source of truth."""
    if not codemap.entries:
        return _AGENTS_HEADER + "\n_(empty — no public symbols)_\n"
    by_id = {e.id: e for e in codemap.entries}
    groups = _group(codemap)
    lines: list[str] = [_AGENTS_HEADER]
    for mod in sorted(groups):
        lines.append(f"\n## {mod}")
        me = by_id.get(mod)
        if me is not None and me.summary:
            lines.append(me.summary)
        g = groups[mod]
        for cls in sorted(g["classes"]):
            ce = by_id.get(f"{mod}.{cls}")
            tail = f" — {ce.summary}" if ce is not None and ce.summary else ""
            lines.append(f"\n- **class {cls}**{tail}")
            for m in g["classes"][cls]:
                sig = m.signature or m.name
                mtail = f" — {m.summary}" if m.summary else ""
                lines.append(f"  - `{sig}`{mtail}")
        for fn in g["funcs"]:
            sig = fn.signature or fn.name
            ftail = f" — {fn.summary}" if fn.summary else ""
            lines.append(f"- `{sig}`{ftail}")
    return "\n".join(lines) + "\n"


def check_agents_sync(project_dir: Path, codemap: CodeMap) -> list[str]:
    """Return a violation if `<project_dir>/AGENTS.md` is missing or has drifted
    (byte-level) from the deterministic render of `codemap` (empty = clean). The
    "verified against CODEMAP" moat: a hand-edit or a CODEMAP change without a
    regenerate makes this erupt. Lives here (not in codemap.py) because it
    depends on `render_agents_md`."""
    path = Path(project_dir) / AGENTS_FILE
    # refuse symlinked deliverables; read through bounded
    # no-follow helper (8 MiB cap — typical AGENTS.md is <1 MiB).
    if path.is_symlink():
        return [f"{AGENTS_FILE}: refusing symlinked deliverable"]
    if not path.is_file():
        return [f"{AGENTS_FILE}: missing (run `peers agents-doc` to generate it)"]
    try:
        # Raw bytes: a true byte comparison (read_text would universal-newline-
        # normalize CRLF→LF and let a CRLF-corrupted file slip past the gate).
        actual = read_bytes_no_symlink(path, max_bytes=8 * 1024 * 1024)
    except OSError as e:
        return [f"{AGENTS_FILE}: unreadable: {e}"]
    if actual != render_agents_md(codemap).encode("utf-8"):
        return [f"{AGENTS_FILE}: out of sync with CODEMAP "
                "(regenerate with `peers agents-doc`)"]
    return []
